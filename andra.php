  <h3 class="mittenbox3-title">Avslutande Fakta</h3>
  <p>Svarta katter är de som har svårast att bli adopterade från djurhem
Tyvärr stämmer det. Svarta katter blir adopterade mer sällan och får ofta vänta längre på ett nytt hem än katter med andra färger. Det finns flera orsaker till detta. 
En stor anledning är fördomarna om att svarta katter skulle vara otursamma eller elaka, vilket gör att många väljer bort dem. Dessutom är svarta katter, 
trots att de är otroligt söta, svårare att fotografera eftersom deras mörka päls gör det svårt att fånga detaljer på bild.

<p>Svarta katter kan vara friskare än andra katter
De genetiska mutationer som ger svarta katter deras vackra mörka päls påverkar mer än bara färgen. 
Forskare tror att samma genetiska faktorer också kan minska risken för att svarta katter utvecklar vissa sjukdomar. Dessutom har forskare kopplat denna gen till den gen som ger människor motståndskraft mot HIV. 
Mer forskning behövs fortfarande — men vem vet? Svarta katter kanske en dag kan bidra till lösningen på HIV hos människor.</p>
